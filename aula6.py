contador = 5  # valor inicial

# operadores de atribuição com o contador
print("Valor inicial:", contador)

contador += 2   # soma -> contador = contador + 2
print("Após += 2:", contador)

contador -= 1   # subtração -> contador = contador - 1
print("Após -= 1:", contador)

contador *= 3   # multiplicação -> contador = contador * 3
print("Após *= 3:", contador)

contador /= 2   # divisão -> contador = contador / 2
print("Após /= 2:", contador)

contador //= 2  # divisão inteira -> contador = contador // 2
print("Após //= 2:", contador)

contador **= 2  # exponenciação -> contador = contador ** 2
print("Após **= 2:", contador)

contador %= 3   # módulo -> contador = contador % 3
print("Após %= 3:", contador)

print("Acabou!")
